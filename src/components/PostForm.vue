<template>
  <form @submit.prevent="submit">
    <input type="text" placeholder="Title"
            v-model='title'>
    <input type="text" placeholder="Body"
            v-model='body'>
    <button type="submit">Create Post</button>
  </form>
</template>

<script>
import {mapMutations} from 'vuex';
export default {
  data() {
    return {
      title: '',
      body: ''
    }
  },
  methods: {
    ...mapMutations(['createPost']),
    submit() {
      this.createPost({
        title: this.title,
        body: this.body,
        id: Date.now()
      });
      this.title = this.body = '';
    }
  }
}
</script>

<style scoped>
input {
  display: block;
  margin: 10px auto;
  width: 100%;
}
</style>